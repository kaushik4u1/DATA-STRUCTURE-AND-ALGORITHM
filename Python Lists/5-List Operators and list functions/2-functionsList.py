#function Operators.

a = [0,1,2,3,4,5,6,7]
print(len(a)) #The len() function returns the number of items (length) in an object.
print(max(a)) #max() function returns the largest item in an iterable. It can also be used to find the largest item between two or more parameters.
print(min(a)) #min() function returns the smallest item in an iterable. It can also be used to find the smallest item between two or more parameters.
print(sum(a)) #The sum() function adds the items of an iterable and returns the sum.

