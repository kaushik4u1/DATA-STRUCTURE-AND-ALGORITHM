#How to create a dictionary.

engTosp = {"one":"uno","two":"dos","three":"tres"} #dict{}-can use to create a dictionary.
print(engTosp)

#Output: {'one': 'uno', 'two': 'dos', 'three': 'tres'}

#If we want to access the value of first key:
print(engTosp['one'])

#Output: uno


#Time Complexity:O(1)
#Space Complexity:O(1)